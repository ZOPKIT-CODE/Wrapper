/**
 * Human-readable labels for plan `applications` / `modules` codes (aligned with backend matrix naming).
 */

const APP_LABELS: Record<string, string> = {
  crm: 'CRM',
  hr: 'HR',
  accounting: 'Financial Accounting',
  project_management: 'Project Management',
  affiliateConnect: 'Affiliate Connect',
  operations: 'Operations',
}

export function appDisplayName(code: string): string {
  return APP_LABELS[code] ?? code.replace(/_/g, ' ').replace(/\b\w/g, (c) => c.toUpperCase())
}

